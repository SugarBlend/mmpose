from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path
from typing import Optional
import yaml


@dataclass
class ModelConfig:
    """
    Configuration for a single model evaluation run.

    Attributes
    ----------
    legend:
        Human-readable label shown in the radar plot legend.
    model_path:
        Path to the deployed model weights (e.g. .onnx / .pth).
    config_path:
        Path to the mmpose config file.
    dataset_folder:
        Root folder containing the images referenced by ann_file.
    ann_file:
        Path to the COCO-style annotation JSON.
    anns_schema:
        Skeleton format of the annotation file. Must be registered in
        skeletons.SKELETON_SUBSETS (e.g. "coco_wholebody", "halpe136").
    expected_joints:
        number of keypoints which will be generated by current model
    converter:
        mapping from source to target
    """

    legend: str
    model_path: str
    config_path: str
    dataset_folder: str
    ann_file: str
    expected_joints: int
    converter: str | None = None
    anns_schema: str = "coco_wholebody"

    def __post_init__(self) -> None:
        # Basic path validation (warn, not crash — paths may be on remote FS)
        for attr in ("model_path", "config_path", "dataset_folder", "ann_file"):
            p = Path(getattr(self, attr))
            if not p.exists():
                import warnings
                warnings.warn(f"[ModelConfig] '{attr}' does not exist: {p}", stacklevel=2)


@dataclass
class EvalConfig:
    """
    Top-level evaluation configuration loaded from YAML.

    Attributes
    ----------
    models:
        List of ModelConfig instances to evaluate in sequence.
    radar_xticks:
        Y-axis ticks for the radar plot (values 0–1).
    save_dir:
        Directory where metrics.json and radar.png will be saved.
        None means do not save.
    show_plot:
        Whether to display the interactive radar plot window.
    """

    models: list[ModelConfig]
    radar_xticks: Optional[list[float]] = None
    save_dir: Optional[str] = None
    show_plot: bool = True

    @classmethod
    def load(cls, path: str) -> "EvalConfig":
        raw = yaml.safe_load(Path(path).read_text(encoding="utf-8"))

        models = []
        for model in raw.get("models", []):
            models.append(ModelConfig(**model))

        return cls(
            models=models,
            radar_xticks=raw.get("radar_xticks"),
            save_dir=raw.get("save_dir"),
            show_plot=raw.get("show_plot", True),
        )

    def dump(self, path: str) -> None:
        data = {
            "models": [
                {
                    "legend": m.legend,
                    "model_path": m.model_path,
                    "config_path": m.config_path,
                    "dataset_folder": m.dataset_folder,
                    "ann_file": m.ann_file,
                    "anns_schema": m.anns_schema,
                    "expected_joints": m.expected_joints,
                    "converter": m.converter,
                }
                for m in self.models
            ],
            "radar_xticks": self.radar_xticks,
            "save_dir": self.save_dir,
            "show_plot": self.show_plot,
        }
        Path(path).write_text(yaml.dump(data, default_flow_style=False, allow_unicode=True))
